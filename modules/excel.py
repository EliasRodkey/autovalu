    @staticmethod
    def write_cells(sheet, cell_range, data):
        # logging.debug('mapping data: {} to cells {} through {}'.format(data, cell_range[0], cell_range[1]))
        cells = sheet[cell_range[0] : cell_range[1]]
        if len(cells) != len(data):
            logging.error(
                "data len: {} does fit cell range len {}".format(len(data), len(cells))
            )
            raise "Data Error: Data does not fit cell range"
        for y, row in enumerate(cells):
            if len(row) != len(data[0]):
                logging.error(
                    "data len: {}, index: {} does fit cell range len {}".format(
                        len(data[0]), y, len(cells)
                    )
                )
                raise "Data Error: row len {} in data does not fit cell range".format(
                    len(row)
                )
            for x, cell in enumerate(row):
                try:
                    cell.value = data[y][x]
                except Exception as error:
                    print(data[y])
                    print(error)

    @staticmethod
    def set_cell_size(sheet, data, start_column):
        start_index = utils.column_index_from_string(start_column)
        column_widths = []
        for row in data:
            for i, cell in enumerate(row):
                try:
                    if cell[0] == "=":
                        length = len(cell) / 2
                    else:
                        length = len(str(cell))
                except IndexError:
                    length = 0
                except TypeError:
                    length = len(str(cell))

                if len(column_widths) > i:
                    if length > column_widths[i]:
                        column_widths[i] = length
                else:
                    column_widths += [len(str(cell))]

        for i, column_width in enumerate(column_widths):
            sheet.column_dimensions[
                utils.get_column_letter(start_index + i)
            ].width = column_width

    @staticmethod
    def merge_center(sheet, cell_ranges):
        for cell_range in cell_ranges:
            sheet.merge_cells(cell_range)

    @staticmethod
    def set_cell_type(sheet, cell_ranges, form):
        for ranges in cell_ranges:
            cells = sheet[ranges[0] : ranges[1]]
            for row in cells:
                for cell in row:
                    cell.number_format = form

    @staticmethod
    def set_cell_justify(sheet, cells, justify):
        for item in cells:
            if len(item) > 1:
                cell_range = sheet[item[0] : item[1]]
                for row in cell_range:
                    for cell in row:
                        cell.alignment = Alignment(**justify)
            else:
                sheet[item[0]].alignment = Alignment(**justify)

    @staticmethod
    def set_font_size(sheet, cells, font):
        for item in cells:
            if len(item) > 1:
                cell_range = sheet[item[0] : item[1]]
                for row in cell_range:
                    for cell in row:
                        cell.font = Font(**font)
            else:
                sheet[item[0]].font = Font(**font)

    @staticmethod
    def set_borders(sheet, cells, border):
        for item in cells:
            if len(item) > 1:
                cell_range = sheet[item[0] : item[1]]
                for row in cell_range:
                    for cell in row:
                        cell.border = border
            else:
                sheet[item[0]].border = border

    @staticmethod
    def outside_border(sheet, cell_ranges, bordert):
        for rang in cell_ranges:
            if len(rang) == 1:
                sheet[rang[0]].border = bordert["all"]
            else:
                cells = sheet[rang[0] : rang[1]]
                for y, row in enumerate(cells):
                    for x, cell in enumerate(row):
                        if len(row) == 1:
                            if y == 0:
                                cell.border = bordert["bottom_open"]
                            elif y == len(cells) - 1:
                                cell.border = bordert["top_open"]
                            else:
                                cell.border = bordert["left_right"]
                        elif len(cells) == 1:
                            if x == 0:
                                cell.border = bordert["right_open"]
                            elif x == len(row) - 1:
                                cell.border = bordert["left_open"]
                            else:
                                cell.border = bordert["top_bottom"]
                        else:
                            if y == 0:
                                if x == 0:
                                    cell.border = bordert["top_left"]
                                elif x == len(row) - 1:
                                    cell.border = bordert["top_right"]
                                else:
                                    cell.border = bordert["top"]
                            elif (x == 0) and ((y != 0) and (y != len(cells) - 1)):
                                cell.border = bordert["left"]
                            elif (x == len(row) - 1) and (
                                (y != 0) and (y != len(cells) - 1)
                            ):
                                cell.border = bordert["right"]
                            elif y == len(cells) - 1:
                                if x == 0:
                                    cell.border = bordert["bottom_left"]
                                elif x == len(row) - 1:
                                    cell.border = bordert["bottom_right"]
                                else:
                                    cell.border = bordert["bottom"]

    @staticmethod
    def set_cell_color(sheet, cells, color):
        for item in cells:
            if len(item) > 1:
                cell_range = sheet[item[0] : item[1]]
                for row in cell_range:
                    for cell in row:
                        cell.fill = color
            else:
                sheet[item[0]].fill = color


